package com.transport.urbain.model;

/**
 * Enum representing days of the week.
 * <p>
 * Used to specify which days a schedule operates. Allows flexibility
 * in defining operational schedules beyond standard patterns.
 */
public enum DayOfWeek {
    /**
     * Monday
     */
    MONDAY,

    /**
     * Tuesday
     */
    TUESDAY,

    /**
     * Wednesday
     */
    WEDNESDAY,

    /**
     * Thursday
     */
    THURSDAY,

    /**
     * Friday
     */
    FRIDAY,

    /**
     * Saturday
     */
    SATURDAY,

    /**
     * Sunday
     */
    SUNDAY
}
